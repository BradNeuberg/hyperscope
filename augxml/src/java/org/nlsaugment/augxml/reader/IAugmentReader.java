package org.nlsaugment.augxml.reader;

import org.nlsaugment.augxml.model.AugmentFile;

/**
 * The interface that all Augment readers must implement. Implementations will
 * read a file-based representation of an AugmentFile and return a AugmentFile object graph.
 * @author Jonathan Cheyer
 */
public interface IAugmentReader {
  
  /**
   * Load a file-based representation of an Augment file and return an AugmentFile object graph.
   * @param filename the name of the file from which to read
   * @return the AugmentFile object graph
   */
  public AugmentFile load(String filename);
}
